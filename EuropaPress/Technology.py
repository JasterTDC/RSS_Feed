
# Include datetime library.
import datetime

# Technology class.
class Tecnology:

    # Class constructor.
    def __init__(self, title, description, date, link, category):
        self.title = title
        self.description = description
        self.date = date
        self.link = link
        self.category = category

    # Title getter.
    def getTitle () :
        return self.title

    # Description getter.
    def getDescription () :
        return self.description

    # Date getter.
    def getDate () :
        return self.date

    # Link getter.
    def getLink () :
        return self.link

    # Category list getter.
    def getCategory () :
        return self.category
